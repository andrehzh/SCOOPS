import React, { useEffect, useState } from "react";
import { View, Text, SafeAreaView, ScrollView } from "react-native";
import { useSelector } from "react-redux";
import LottieView from "lottie-react-native";
import firebase from "../firebase";
import ProductItems from "../components/shopDetail/ProductItems";
import { goods } from "./ShopDetail";

export default function OrderCompleted() {
  const [lastOrder, setLastOrder] = useState({
    products: [
      {
        title: "Bologna",
        description: "With butter lettuce, tomato and sauce bechamel",
        price: "$13.50",
        image:
          "https://www.modernhoney.com/wp-content/uploads/2019/08/Classic-Lasagna-14-scaled.jpg",
      },
    ],
  });

  const { products, shopName } = useSelector(
    (state) => state.cartReducer.selectedProducts
  );

  const total = products
    .map((product) => Number(product.price.replace("$", "")))
    .reduce((prev, curr) => prev + curr, 0);

  const totalUSD = total.toLocaleString("en", {
    style: "currency",
    currency: "SGD",
  });

  useEffect(() => {
    const db = firebase.firestore();
    const unsubscribe = db
      .collection("orders")
      .orderBy("createdAt", "desc")
      .limit(1)
      .onSnapshot((snapshot) => {
        snapshot.docs.map((doc) => {
          setLastOrder(doc.data());
        });
      });

    return () => unsubscribe();
  }, []);

  return (
    <SafeAreaView style={{ flex: 1, backgroundColor: "white" }}>
      {/* green checkmark */}
      <View
        style={{
          margin: 15,
          alignItems: "center",
          height: "100%",
        }}
      >
        {/* <LottieView
          style={{ height: 100, alignSelf: "center", marginBottom: 30 }}
          source={require("../assets/animations/check-mark.json")}
          autoPlay
          speed={0.5}
          loop={false}
        /> */}
        <Text style={{ fontSize: 20, fontWeight: "bold" }}>
          Your order at {shopName} has been placed for {totalUSD}
        </Text>
        <ScrollView>
          <ProductItems
            goods={lastOrder.products}
            hideCheckbox={true}
            marginLeft={10}
          />
          {/* <LottieView
            style={{ height: 200, alignSelf: "center" }}
            source={require("../assets/animations/cooking.json")}
            autoPlay
            speed={0.5}
          /> */}
        </ScrollView>
      </View>
    </SafeAreaView>
  );
}
